import random

# 2023年亩产量
yield_2023 = [
    400, 500, 400, 350, 415, 800, 1000, 400, 630, 525,
    110, 3000, 2200, 420, 525, 380, 475, 380, 330, 395,
    760, 950, 380, 600, 500, 105, 2850, 2100, 400, 500,
    360, 450, 360, 315, 375, 720, 900, 360, 570, 475,
    100, 2700, 2000, 380, 475, 500, 3000, 2000, 3000,
    2000, 2400, 6400, 2700, 2400, 3300, 3700, 4100, 3200,
    12000, 4100, 1600, 10000, 5000, 5500, 3600, 2400,
    3600, 2400, 3000, 8000, 3300, 3000, 4000, 4500, 5000,
    4000, 15000, 5000, 2000, 12000, 6000, 6600, 5000,
    4000, 3000, 5000, 4000, 10000, 1000, 3200, 2200,
    3200, 2200, 2700, 7200, 3000, 2700, 3600, 4100,
    4500, 3600, 13500, 4500, 1800, 11000, 5400, 6000
]

# 存储未来八年的亩产量
future_yields = []

# 计算2024到2030年的预期亩产量
for year in range(8):  # 未来八年
    year_yields = []
    for i in range(len(yield_2023)):
        # 生成随机增长率在-10%到10%之间
        growth_rate = random.uniform(-0.10, 0.10)

        # 计算预期亩产量
        if year == 0:
            new_yield = yield_2023[i]  # 第一年亩产量为2023年的亩产量
        else:
            new_yield = future_yields[year - 1][i] * (1 + growth_rate)  # 计算未来亩产量

        year_yields.append(new_yield)

    future_yields.append(year_yields)

# 输出结果
for year in range(8):
    print(f"Year {2023 + year}:")
    for i in range(len(future_yields[year])):
        print(f"  Crop {i + 1}: Yield = {future_yields[year][i]:.2f}")